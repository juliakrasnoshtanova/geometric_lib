def area(a, b, c):
    '''Принимает числа a, b, c (стороны равностороннего треугольника); находит площадь равностороннего треугольника'''
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''Принимает числа a, b, c (стороны треугольника); находит периметр треугольника'''
    return a + b + c
